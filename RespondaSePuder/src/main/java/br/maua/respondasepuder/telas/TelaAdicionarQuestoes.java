    private void adicionarQuestaoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarQuestaoButtonActionPerformed
        // Pegar o texto do enunciado
        this.enunciado = enunciadoTextField.getText();
        // Pegar os textos das alternativas
        this.alternativa1 = alternativa1TextField.getText();
        this.alternativa2 = alternativa2TextField.getText();
        this.alternativa3 = alternativa3TextField.getText();
        this.alternativa4 = alternativa4TextField.getText();
        this.alternativa5 = alternativa5TextField.getText();
        // Pegar a informação se cada alternativa está correta
        this.alternativa1EhCorreta = alternativa1RadioButton.isSelected();
        this.alternativa2EhCorreta = alternativa2RadioButton.isSelected();
        this.alternativa3EhCorreta = alternativa3RadioButton.isSelected();
        this.alternativa4EhCorreta = alternativa4RadioButton.isSelected();
        this.alternativa5EhCorreta = alternativa5RadioButton.isSelected();
        // Pegar a matéria
        this.materia =  (Materia) materiaComboBox.getSelectedItem();
        // Modelo de objetos para cadastro na base
        var dao = new MateriaDAO();
        try{
            int idMateria = dao.obterMateriaId(materia);
            var mater = Materia.builder()
                    .identificador(idMateria)
                    .build();
            var questao = Questao.builder()
                .enunciado(enunciado)
                .nivel(nivel)
                .materia(mater)
                .build();
            var alter1 = Alternativa.builder()
                    .texto(alternativa1)
                    .build();
            var alter2 = Alternativa.builder()
                    .texto(alternativa2)
                    .build();
            var alter3 = Alternativa.builder()
                    .texto(alternativa3)
                    .build();
            var alter4 = Alternativa.builder()
                    .texto(alternativa4)
                    .build();
            var alter5 = Alternativa.builder()
                    .texto(alternativa5)
                    .build();
            var qa1 = new QuestaoAlternativa(
                questao,
                alter1,
                alternativa1EhCorreta
            );
            var qa2 = new QuestaoAlternativa(
                questao,
                alter2,
                alternativa2EhCorreta
            );
            var qa3 = new QuestaoAlternativa(
                questao,
                alter3,
                alternativa3EhCorreta
            );
            var qa4 = new QuestaoAlternativa(
                questao,
                alter4,
                alternativa4EhCorreta
            );
            var qa5 = new QuestaoAlternativa(
                questao,
                alter5,
                alternativa5EhCorreta
            );
            List <QuestaoAlternativa> alternativas = new ArrayList<>();
            alternativas.add(qa1);
            alternativas.add(qa2);
            alternativas.add(qa3);
            alternativas.add(qa4);
            alternativas.add(qa5);
            questao.setAlternativas(alternativas);
        
            var queDAO = new QuestaoDAO();
            queDAO.adicionarQuestao(questao, idMateria);
            
            var altDAO = new AlternativaDAO();
            altDAO.adicionarAlternativa(alter1);
            altDAO.adicionarAlternativa(alter2);
            altDAO.adicionarAlternativa(alter3);
            altDAO.adicionarAlternativa(alter4);
            altDAO.adicionarAlternativa(alter5);
            
            var queAltDAO = new QuestaoAlternativaDAO();
            queAltDAO.adicionarQuestaoAlternativa(qa1);
            queAltDAO.adicionarQuestaoAlternativa(qa2);
            queAltDAO.adicionarQuestaoAlternativa(qa3);
            queAltDAO.adicionarQuestaoAlternativa(qa4);
            queAltDAO.adicionarQuestaoAlternativa(qa5);
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro ao adicionar questão: " + ex.getMessage());
        }
    }//GEN-LAST:event_adicionarQuestaoButtonActionPerformed

